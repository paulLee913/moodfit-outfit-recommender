// 추천 생성 함수
function generateRecommendation(weather, situation, mood) {
  const key = `${weather}_${situation}`;
  
  if (!recommendationData[key]) {
    return {
      error: "해당 조건의 추천을 찾을 수 없습니다."
    };
  }
  
  const situationData = recommendationData[key];
  const recommendation = situationData[mood];
  
  if (!recommendation) {
    return {
      error: "해당 분위기의 추천을 찾을 수 없습니다."
    };
  }
  
  return {
    weather,
    situation,
    mood,
    ...recommendation
  };
}

